import string

chars = string.printable

arr = [0x43,0xa4,0x41,0xae,0x42,0xfc,0x73,0xb0,0x6f,0x72,0x5e,0xa8,0x65,0xf2,0x51,0xce,0x20,0xbc,0x60,0xa4,0x6d,0x46,0x21,0x40,0x20,0x5a,0x2c,0x52,0x2d,0x5e,0x2d,0xc4]
answer = ""

print(len(arr))
count = 0;
for j in arr:
    for i in chars:
        temp = (ord(i) ^ count ) * (count % 2 + 1)
        if temp == j:
            count+=1
            answer = answer + i
            break

print(answer)
    
